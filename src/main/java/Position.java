import javafx.geometry.Pos;

public class Position {
    public int x,y;
    public Pos p;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setP(Pos p) {
        this.p = p;
    }

    public Pos getP() {
        return p;
    }
}
